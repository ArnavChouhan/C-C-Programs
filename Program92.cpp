// WAP to Find Square of Numbers from 1 to n
#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n;

    for(int i=1;i<=n;i++)
        cout << i*i << " ";

    return 0;
}


// WAP to Print Cube of Numbers from 1 to  n
#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n;

    for(int i=1;i<=n;i++)
        cout << i*i*i << " ";

    return 0;
}
